// Nav scroll effect
const nav = document.getElementById('nav');
window.addEventListener('scroll', () => { nav.classList.toggle('scrolled', window.scrollY > 60); });

// Scroll animations
const observer = new IntersectionObserver((entries) => {
  entries.forEach(entry => { if (entry.isIntersecting) entry.target.classList.add('visible'); });
}, { threshold: 0.12 });
document.querySelectorAll('.animate-in').forEach(el => observer.observe(el));

// Hero terminal tab switching
function heroTab(tab) {
  document.querySelectorAll('.ht-tab').forEach(t => t.classList.remove('active'));
  document.querySelectorAll('.ht-panel').forEach(p => p.classList.remove('active'));
  document.querySelector(`[onclick="heroTab('${tab}')"]`).classList.add('active');
  document.getElementById(`hero-${tab}`).classList.add('active');
}

// Library terminal tab switching
function switchTab(tab) {
  document.querySelectorAll('.terminal-tab').forEach(t => t.classList.remove('active'));
  document.querySelectorAll('.t-panel').forEach(p => p.classList.remove('active'));
  document.querySelector(`[onclick="switchTab('${tab}')"]`).classList.add('active');
  document.getElementById(`tab-${tab}`).classList.add('active');
}

// Load portal stats from static JSON
// This file is generated by: quantms-portal web build ...
// and committed to the quantms.org repo under data/portal-stats.json
// In production, can also point to https://portal.quantms.org/data/portal-stats.json
const PORTAL_STATS_URL = 'data/portal-stats.json';

function loadPortalStats() {
  fetch(PORTAL_STATS_URL)
    .then(r => r.ok ? r.json() : Promise.reject('not found'))
    .then(stats => {
      document.querySelectorAll('[data-stat]').forEach(el => {
        const key = el.getAttribute('data-stat');
        if (stats[key] !== undefined) {
          el.setAttribute('data-target', stats[key]);
        }
      });
      // Re-trigger counter animations now that data-target values are set
      initStatCounters();
    })
    .catch(() => {
      // Fallback: show dashes if stats unavailable
      document.querySelectorAll('[data-stat]').forEach(el => {
        el.textContent = '—';
      });
    });
}

// Stat counter animation
function formatStat(value) {
  if (value >= 1000000) return (value / 1000000).toFixed(1) + 'M';
  if (value >= 1000) return (value / 1000).toFixed(0) + 'K';
  return value.toString();
}

function initStatCounters() {
  const statObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const el = entry.target;
        const target = parseInt(el.getAttribute('data-target'));
        if (!target || el.dataset.counted) return;
        el.dataset.counted = 'true';
        const duration = 2000;
        const start = performance.now();
        function update(now) {
          const elapsed = now - start;
          const progress = Math.min(elapsed / duration, 1);
          const eased = 1 - Math.pow(1 - progress, 3);
          const current = Math.floor(eased * target);
          el.textContent = formatStat(current);
          if (progress < 1) requestAnimationFrame(update);
          else el.textContent = formatStat(target);
        }
        requestAnimationFrame(update);
      }
    });
  }, { threshold: 0.5 });
  document.querySelectorAll('[data-target]').forEach(el => statObserver.observe(el));
}

// Load stats on page load, then init counters
loadPortalStats();
